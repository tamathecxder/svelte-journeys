<script>
    import { roll } from './utils';

    let promise = $state(roll());
</script>

<button onclick={() => promise = roll()}>
    roll the dice
</button>

{#await promise}
    <p>...rolling</p>
{:then number}
    <p>you rolled a {number}</p>
{:catch error}
    <p style="color: red">{error.message}</p>
{/await}